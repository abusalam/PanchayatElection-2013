) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS `PE2013_Visitors` (
  `ip` tinytext,
  `vtime` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `vpage` tinytext,
  `referrer` tinytext,
  `uagent` text
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

